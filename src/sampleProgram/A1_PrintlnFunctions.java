package sampleProgram;

public class A1_PrintlnFunctions {
	
	public static void main(String[] args) {
		
		System.out.println("Hello Hi Welcome");
		System.out.println(1+1); // 2
		System.out.println(1+"HI"); // 1HI
		System.out.println("1"+"1"); //11
		System.out.println(1+"1"+1); //111
		System.out.println(1+1+"1"); //21
		System.out.println(1+1+1);  //3
		System.out.println("1"+1+"1"); //111
		System.out.println(1+(1+1)); //3
		System.out.println("!"+"!"); //!!
		System.out.println("1"+1+1);  //111 -we combine integer to string, so it will take following items also as string
		
		/*
		 * println() method whose job is to print data on the console. 
		 * This method is overloaded to accept all kinds of data types in Java. 
		 * You have the println() method which takes String, int, float, double, or even char in output. 
		 * All of those methods are collectively referred to as an overloaded method in Java.
		 * String is the largest datatype
		 * in java, all functions check from left to right
		 */
		
		
		
	}

}
